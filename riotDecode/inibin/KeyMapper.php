    protected static $cachedKeyMapping;

    public static function generateStructureFromLogile($logFile) {
        $inibinGroups = [];

        $varName = null;
        $group = null;

        foreach(file($logFile) as $line) {
            if($varName !== null and $group !== null) {
                if(!key_exists($group, $inibinGroups)) $inibinGroups[$group] = [];

                $inibinGroups[$group][$varName] = $varName;

                $varName = null;
                $group = null;
            }

            if(preg_match('/EBP: = [^"]+"([^"]+)/', $line, $matches)) {
                $varName = $matches[1];
            } elseif(preg_match('/EDI: = [^"]+"([^"]+)/', $line, $matches)) {
                $group = $matches[1];
            }
        }

        if($varName !== null and $group !== null) {
            if(!key_exists($group, $inibinGroups)) $inibinGroups[$group] = [];

            $inibinGroups[$group][$varName] = $varName;

            $varName = null;
            $group = null;
        }

        $result = '[' . "\n";
        foreach($inibinGroups as $groupName => $varNames) {
            $result .= "    '" . $groupName . "' => [\n";
            $result .= "        '" . implode("',\n        '", $varNames) . "'\n";
            $result .= "    ],\n";
        }
        $result = substr($result, 0, strlen($result) - 2) . "\n]";

        return $result;
    }

    public static function getNumericInibinKey($sectionName = null, $varName = null) {
        $hashValue = 0;
        if($sectionName != null) {
            foreach(str_split($sectionName) as $char) $hashValue = (int) (ord(strtolower($char)) + 65599 * $hashValue);

            $hashValue = (int) (65599 * $hashValue + 42);
            if($varName !== null) {
                foreach(str_split($varName) as $char) $hashValue = (int) (ord(strtolower($char)) + 65599 * $hashValue);
            }
        }

        return sprintf('%u', $hashValue);
    }

    public static function getStringInibinKey($numericKey) {
        if(self::$cachedKeyMapping === null) {
            self::$cachedKeyMapping = [];

            foreach(self::$inibinStructure as $group => $vars) {
                foreach($vars as $var) {
                    self::$cachedKeyMapping[self::getNumericInibinKey($group, $var)] = [$group, $var];
                }
            }
        }

        return key_exists($numericKey, self::$cachedKeyMapping) ? self::$cachedKeyMapping[$numericKey] : null;
    }
